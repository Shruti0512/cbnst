/*Find whether roots of the equations is real,imaginary,equal.*/


#include<stdio.h>
#include<conio.h>
int main()
{
    double a,b,c;
    scanf("%lf %lf %lf",&a,&b,&c);
    double d=(b*b)-(4*a*c);
    if(d==0)
    {
        printf("Equal roots");
    }
    else if(d<0)
    {
        printf("imaginary roots");
    }
    else{
        printf("real roots");
    }
    return 0;
}
